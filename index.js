function add(i, m){
    return i +m;
}

function subtract(i, m){
    return i - m;
}

function multiply(i, m){
    return i * m;
}

function divide(i, m){
    return i / m;
}

function increment(n){
    return n+= 1;
}

function decrement(n){
    return n -= 1;
}

function makeInt(n){

    return parseInt(n, 10);
}
function preserveDecimal(n){
    return parseFloat(n);
}